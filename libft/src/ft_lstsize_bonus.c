#include "../includes/libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	if (!lst)
		return (i);
	while (lst && lst->next != NULL)
	{
		lst = lst->next;
		i++;
	}
	i++;
	return (i);
}
