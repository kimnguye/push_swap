#include "../includes/libft.h"

int	ft_isalnum(int c)
{
	if (('0' <= c && c <= '9'))
		return (8);
	else if (('A' <= c && 'Z' >= c) || ('a' <= c && 'z' >= c))
		return (8);
	return (0);
}
